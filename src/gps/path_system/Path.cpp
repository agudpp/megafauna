#include "Path.h"

namespace gps {

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
Path::Path()
{
}

////////////////////////////////////////////////////////////////////////////////
Path::~Path()
{

}

////////////////////////////////////////////////////////////////////////////////
void
Path::pushNode(const Point& pos)
{
    debugWARNING("Check that pos doesn't exists already\n");
    mNodes.push_back(Node(pos));
}

////////////////////////////////////////////////////////////////////////////////
void
Path::removeLast(void)
{
    if (mNodes.empty()) {
        return;
    }
    mNodes.pop_back();
}

////////////////////////////////////////////////////////////////////////////////
void
Path::clear(void)
{
    mNodes.clear();
}

}
